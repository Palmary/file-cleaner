function truncateOutput(string $output, int $maxLength): string
{
    if (strlen($output) <= $maxLength) {
        return $output;
    }
    return substr($output, 0, $maxLength) . "\n... [output truncated]";
}

function removeQuarantineSession(string $sessionDir, string $quarantineDir): void
{
    if (!is_dir($sessionDir) || realpath($sessionDir) === realpath($quarantineDir)) {
        return;
    }
    if (!str_starts_with(realpath($sessionDir), realpath($quarantineDir))) {
        throw new RuntimeException('Session directory is outside quarantine root');
    }
    $files = new RecursiveIteratorIterator(
        new RecursiveDirectoryIterator($sessionDir, RecursiveDirectoryIterator::SKIP_DOTS),
        RecursiveIteratorIterator::CHILD_FIRST
    );
    foreach ($files as $file) {
        $file->isDir() ? rmdir($file->getRealPath()) : unlink($file->getRealPath());
    }
    rmdir($sessionDir);
}

if ($action === 'delete_reports') {
    $files = $_POST['report_files'] ?? [];
    $returnPage = filter_input(INPUT_POST, 'return_page', FILTER_VALIDATE_INT) ?: 1;
    if (!is_array($files) || empty($files)) {
        $_SESSION['alert'] = ['type' => 'warning', 'message' => __('no_reports_selected')];
        header('Location: reports.php?page=' . $returnPage);
        exit;
    }

    runAndRedirect(
        static function () use ($files, $config): array {
            $reportDir = realpath($config['report_dir']);
            if ($reportDir === false) {
                throw new RuntimeException('Report directory is not accessible');
            }

            $deleted = 0;
            $failed = [];
            foreach ($files as $filename) {
                $filename = basename((string)$filename);
                if (!preg_match('/^(report-\d{8}-\d{6})\.(json|html|csv)$/', $filename, $matches)) {
                    $failed[] = "{$filename}: invalid filename";
                    continue;
                }
                $prefix = $matches[1];
                $extensions = ['json', 'html', 'csv'];
                foreach ($extensions as $ext) {
                    $path = $reportDir . '/' . $prefix . '.' . $ext;
                    $real = realpath($path);
                    if ($real === false || !str_starts_with($real, $reportDir)) {
                        continue;
                    }
                    if (unlink($real)) {
                        $deleted++;
                    } else {
                        $failed[] = "{$prefix}.{$ext}: cannot delete";
                    }
                }
            }

            return [
                'success' => empty($failed),
                'deleted' => $deleted,
                'failed' => $failed,
            ];
        },
        __('reports_deleted'),
        __('reports_delete_failed'),
        'reports.php?page=' . $returnPage
    );
}

