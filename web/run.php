$isAjax = ($_SERVER['HTTP_X_REQUESTED_WITH'] ?? '') === 'XMLHttpRequest';

function runAndRedirect(callable $callback, string $successMessage, string $errorMessage, string $redirectUrl): never
{
    global $isAjax;
    try {
        $result = $callback();
        $ok = is_array($result) && !empty($result['success']);
        if (!$ok) {
            $detail = is_array($result) && isset($result['output']) && $result['output'] !== ''
                ? $result['output']
                : (is_array($result) ? json_encode($result, JSON_UNESCAPED_SLASHES) : '');
            $message = $detail !== '' ? $errorMessage . "\n\n" . truncateOutput($detail, 1000) : $errorMessage;
            if ($isAjax) {
                jsonResponse(['success' => false, 'error' => $message, 'result' => $result], 500);
            }
            $_SESSION['alert'] = ['type' => 'danger', 'message' => $message];
            header('Location: ' . $redirectUrl);
            exit;
        }
        if ($isAjax) {
            jsonResponse(['success' => true, 'result' => $result]);
        }
        $_SESSION['alert'] = ['type' => 'success', 'message' => $successMessage];
    } catch (Throwable $e) {
        if ($isAjax) {
            jsonResponse(['success' => false, 'error' => $e->getMessage()], 500);
        }
        $_SESSION['alert'] = ['type' => 'danger', 'message' => $e->getMessage()];
    }
    header('Location: ' . $redirectUrl);
    exit;
}

if ($action === 'scan') {
    try {
        $configPath = $_POST['config_path'] ?? $config['default_config_path'] ?? '';
        validateConfigPath((string)$configPath, $config['allowed_config_prefixes']);

        $format = in_array($_POST['format'] ?? '', ['text', 'json', 'csv', 'html'], true)
            ? $_POST['format']
            : ($config['default_report_format'] ?? 'html');
        $dryRun = !empty($_POST['dry_run']);

        set_time_limit($config['max_execution_time']);

        $runner = new FileCleanerRunner(binaryPath($config), $config['max_execution_time']);
        $result = $runner->scan($configPath, $format, $dryRun);

        if ($isAjax) {
            jsonResponse($result);
        }

        // Find the latest JSON report generated by the scan.
        $reports = listReports($config['report_dir']);
        $latestJson = null;
        foreach ($reports as $report) {
            if ($report['type'] === 'json') {
                $latestJson = $report['filename'];
                break;
            }
        }

        $truncated = truncateOutput($result['output'], 2000);
        if ($result['success']) {
            $_SESSION['alert'] = [
                'type' => 'success',
                'message' => __('scan_completed', [
                    'exit_code' => (string)$result['exit_code'],
                    'report' => $latestJson ? $latestJson : '',
                ]),
            ];
        } else {
            $_SESSION['alert'] = [
                'type' => 'danger',
                'message' => __('scan_failed', [
                    'exit_code' => (string)$result['exit_code'],
                ]) . "\n\n" . $truncated,
            ];
        }
    } catch (Throwable $e) {
        if ($isAjax) {
            jsonResponse(['success' => false, 'error' => $e->getMessage()], 500);
        }
        $_SESSION['alert'] = ['type' => 'danger', 'message' => $e->getMessage()];
    }

    header('Location: index.php');
    exit;
}

